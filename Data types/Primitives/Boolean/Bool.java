import java.lang.*;

class Bool {
    public static void main(String args[]) {
        // Boolean data type is a data type that has one of two possible values (usually
        // denoted true and false).
        // Setting boolean to false and true initially
        boolean a = false;
        boolean b = true;

        if (a == false) {
            System.out.println("Hello Nik");
        }
        if (b == true) {
            System.out.println("Hii Mik..");
        }
       
        if (a == true) {
            System.out.println("Hii Tik...");
        }
        if (b == false) {
            System.out.println("Hii chik...");
        }
    }
}