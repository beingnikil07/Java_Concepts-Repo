class Single {
    public static void main(String[] args) {
        int A[] = new int[5]; // creating an array
        // initialization of arrays
        A[0] = 10;
        A[1] = 20;
        A[2] = 30;
        A[3] = 40;
        A[4] = 50;
        // Traversing array
        for (int i = 0; i <= 4; i++) {
            System.out.println(A[i]);
        }
    }
}