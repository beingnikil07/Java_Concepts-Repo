class Arithmetic {

    public static void main(String args[]) {
        /*
         * They are used to perform simple arithmetic operations on primitive data
         * types
         * : Multiplication
         * / : Division
         * % : Modulo
         * + : Addition
         * â€“ : Subtraction
         * 
         */

        int a = 10;
        int b = 20;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(b / a);
        System.out.println(a / b);
        System.out.println(b % a);
        System.out.println(a % b);

        /*
         * / Division Operator
         * 
         * Note the operation, a / b in our program. The / operator is the division
         * operator.
         * If we use the division operator with two integers, then the resulting
         * quotient will also be an integer. And, if one of the operands is a
         * floating-point number, we will get the result will also be in floating-point.
         * 
         */

        System.out.println(9/2);
        System.out.println(9.0/2);
        System.out.println(9/2.0);
        System.out.println(9.0/2.0);
    }

}